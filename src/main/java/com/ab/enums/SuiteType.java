package com.ab.enums;

/**
 * <p>The SuiteType enum represents the different types of test suites that can be executed in a test automation framework.
 * </p>
 * @author Andrew Bakheet
 * @version 1.0
 * @project Selenium-Framework
 */
public enum SuiteType {

    /**
     * The REGRESSION suite type represents a regression test suite.
     */
    REGRESSION,

    /**
     * The SMOKE suite type represents a smoke test suite.
     */
    SMOKE,

    /**
     * The SMOKE suite type represents a smoke test suite.
     */
    SANITY,

    /**
     * The MINI_REGRESSION suite type represents a mini-regression test suite.
     */
    MINI_REGRESSION,
}
